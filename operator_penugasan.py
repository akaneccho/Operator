# Tabungan
tabungan = float(input("Masukkan Jumlah Tabungan: "))

# Pengeluaran hari pertama
pengeluaran = float(input("Masukkan pengeluaran hari pertama: "))

# Pengeluaran hari kedua
pengeluaran += float(input("Masukkan pengeluaran hari kedua: "))

# Tabungan dikurangi pengeluaran
tabungan -= pengeluaran

# Tampilkan total tabungan
print("Total tabungan anda sekarang:", tabungan)
